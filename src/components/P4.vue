            全球稳定币面临的主要问题<br />
            全球稳定币推广遭遇较大阻力<br />
            货币作为一般等价物，代表着消费者对于货币本质的信任，对于全球稳定币来说，是对其背后锚定资产的信任。<br/><br/>
        
            从稳定型全球稳定币来看，稳定型全球稳定币最有可能锚定一篮子经济发达的国家或联盟的主权货币，并根据经济格局的变化调整其篮子内主权货币的分布，这种构想是目前已知最稳定的锚定资产组合。但其稳定性也将超越目前的霸权货币——美元，即形成一股“去美元化”力量。其他强势主权货币国家经济发展带来的货币主权提升也成了全球稳定币的“嫁衣”，削弱了部分主权货币在全球贸易中的地位的提升。这也是Libra一经发起便遭受到全球大部分国家抵制的根本原因之一。<br/><br/>
        
