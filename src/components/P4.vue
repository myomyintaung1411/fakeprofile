            全球稳定币仍存漏洞和不稳定风险<br/><br/>
        
            尽管全球稳定币并未大规模用于主流支付，但这一领域已知的漏洞仍在不断增加。如果全球稳定币作为支付手段或价值存储进入金融系统的主流，在多个国家和地区流通，并有可能实现可观的交易量，将给全球金融系统稳定带来更大的风险。<br/><br/>
        
            从货币主权角度看，全球稳定币价格波动将会最先传导至其锚定主权货币上。虽然从设计架构上出发，全球稳定币不太容易受到比特币和其他加密数字货币所经历的那种投机泡沫的影响，但其市值和价格可能会随着投资者的购买和赎回而迅速上升和下降。如果没有额外的私人或公共支持，在市场动荡时期，全球稳定币很可能受到严重的价格折扣或挤兑，导致用于支持其价值的锚定货币同样遭受大甩卖。<br/><br/>
        
            从银行业角度看，全球稳定币将加剧存款竞争。与数字人民币不同，全球稳定币仍具有一定的投资属性。消费者可能将活期账户和支付账户中的部分现金转移至全球稳定币钱包，推升银行贷款成本。<br/><br/>
        
            从金融系统性风险角度看，全球稳定币管理机构是否按照发行约定进行相应的资产储备和资产抵押，是否按照投资约定将资产储备投资于银行存款、短期债券等低风险、低收益的金融资产，更为激进地说，管理机构是否将资产储备挪用为个人用途等问题，都已经在现存的稳定币市场显现，若全球稳定币大范围应用，金融市场系统性风险也将随之推高。<br/><br/>
        
            全球稳定币面临数据隐私保护问题<br/><br/>
        
            全球稳定币的信息披露和全球范围内的监管具有必要性。全球范围内庞大的用户信息和交易数据将披露至何种程度、由哪家国际机构进行监管，使其在提升交易效率的同时，保持其发行愿景中的相对匿名性，这一点在央行数字货币上已经讨论得较为清晰。本国央行数字货币交易包含的用户信息和交易信息都由国家监管机构进行监督、监管和排查，这些交易信息也同样受到国家监管机构的保护。如果全球稳定币受到所有锚定货币国家和交易国家的监管，那么其所谓的相对匿名将是一纸空谈。而在目前愈发区域化的世界格局下，单个国家必定将禁止其本国交易数据交由引入的第三方或国际监管机构进行管理。数据隐私与信息保护的进退维谷也将成为全球稳定币推广的主要制约之一。美国、欧盟、新加坡等多个发达经济体均表态，在全球稳定币监管体系尚未健全，建立统一的审查标准之前，将不会批准全球稳定币在本国运营。<br/><br/>
            全球稳定币发展前景 <br/><br/>
        
            央行数字货币与全球稳定币将此消彼长。全球稳定币的主要愿景是相比于普通法定货币降低支付和结算风险及交易成本，同时相比于其他数字加密货币增强用户对系统和支付的信任。随着世界各国开启央行数字货币研究，我们可以看到，全球稳定币与央行数字货币所提供的支付解决方案是高度相似的。<br/><br/>
        
            通过对比Libra、USDC与数字人民币架构可以发现，全球稳定币与央行数字货币的主要区别在于：一是信用背书。稳定型全球稳定币来源于可调节的一篮子法定货币，而货币（美元）型全球稳定币和央行数字货币来源于单个国家信用，其信用水平将取决于锚定货币的信用。从价值角度来说，稳定型全球稳定币的价值稳定性将优于货币型全球稳定币和央行数字货币。二是发行主体。全球稳定币由其发行公司运营，而央行数字货币由国家央行运营。一般而言，主权国家特别是经济基本面良好的主权国家，其运营规范程度和危机化解程度都将优于发行机构。三是应用范围。各国央行数字货币属于法定货币范围，因此应用范围大多局限在本国范围内，而全球稳定币天然可以实现全球范围的跨境支付交易（见表1）。<br/><br/>
        
